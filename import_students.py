def upload_students():
    lines = csv_data.strip().split("\n")[1:] # Skip header
    reader = csv.reader(lines)

    count = 0
    for row in reader:
        if not row: continue
        reg_id = row[1]
        full_name = row[2]
        dept_id = row[3]

        # Password Logic: FirstName@123
        first_name = full_name.split()[0].capitalize()
        password = f"{first_name}@123"

        # Email Logic: firstname.lastname2026@gmail.com
        name_parts = full_name.split()
        clean_name = "".join(name_parts).lower()
        email = f"{clean_name}2026@gmail.com"

        branch = BRANCH_MAP.get(dept_id, "Unknown")
        year = "Second Year"

        # 1. Create User (Parent)
        user = User(
            id=reg_id,
            username=reg_id,
            email=email,
            password_hash=password,
            full_name=full_name,
            role="student"
        )

        # 2. Create Student (Child)
        student = Student(
            id=reg_id,
            registration_number=reg_id,
            full_name=full_name,
            branch=branch,
            year=year,
            department_id=dept_id
        )

        try:
            db.add(user)
            db.flush()
            db.add(student)
            db.commit()
            count += 1
            print(f"Added Student: {full_name} ({reg_id})")
        except Exception as e:
            db.rollback()
            print(f"Failed to add {reg_id}: {e}")

    print(f"\nDone! Successfully added {count} students.")

if __name__ == "__main__":
    upload_students()
